import Foundation


public class MathData {
    
    func add (x : Int, y : Int) -> Int{
        return x + y
    }
    func substract (x : Int, y : Int) -> Int{
        return x - y
    }
    func multiply (x : Int, y : Int) -> Int{
        return x * y
    }
    func divide (x : Int, y : Int) -> Int{
        return x / y
    }
    
}
